package models;

public class hocky {

	private String maHK;
	private String tenHK;

	public hocky() {

	}

	public hocky(String maHK, String tenHK) {
		this.maHK = maHK;
		this.tenHK = tenHK;
	}

	public String getMaHK() {
		return maHK;
	}

	public void setMaHK(String maHK) {
		this.maHK = maHK;
	}

	public String getTenHK() {
		return tenHK;
	}

	public void setTenHK(String tenHK) {
		this.tenHK = tenHK;
	}

}
